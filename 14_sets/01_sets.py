# Sets are unordered, unique collections (no duplicates) allowed at all

# Creating Sets
fruits = {"Apple","Banana","Cherry"}
print(fruits,type(fruits)) # {'Cherry', 'Apple', 'Banana'} <class 'set'>

s = {3,23,2,11} 
print(s,type(s)) # {3, 2, 11, 23} <class 'set'>
