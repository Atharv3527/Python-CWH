# Here are the 8 main built-in data types in Python with one-line descriptions:
# ->
# int(Intergers) – Represents whole numbers (e.g., 10, -3).
# float – Represents decimal numbers (e.g., 3.14, -0.01).
# bool – Represents truth values: True or False.
# str – Represents text (string of characters), e.g., "hello".
# list – Mutable ordered collection, e.g., [1, 2, "three"].
# tuple – Immutable ordered collection, e.g., (1, 2, "three").
# sets – Unordered collection of unique items, e.g., {1, 2, 3}.
# dict – Collection of key-value pairs, e.g., {"a": 1, "b": 2}

age = 4
print(age)
print(type(age))

cgpa = 8.2
print(cgpa)
print(type(cgpa))

name = "Sam"
print(name)
print(type(name))

is_completed = True
print(is_completed) #can also be False -> Note T and F are Capital in this case
print(type(is_completed))
